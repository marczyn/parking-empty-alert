MQTT_USER=frigate
MQTT_PASSWORD=$MQTT_PASSWORD
WHATSAPP_PHONE=$WHATSAPP_PHONE
WHATSAPP_APIKEY=$WHATSAPP_APIKEY
TZ=$TZ
